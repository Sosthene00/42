#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	size_t	i;
	char	*cpy;

	len = ft_strlen(s1);
	i = 0;
	if ((cpy = malloc(sizeof(*cpy) * (len + 1))) == 0)
		return (NULL);
	while (s1[i])
	{
		cpy[i] = s1[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
