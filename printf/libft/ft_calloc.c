#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;
	size_t	i;

	i = count * size;
	if (!(ptr = malloc(i)))
		return (NULL);
	ft_bzero(ptr, i);
	return (ptr);
}
